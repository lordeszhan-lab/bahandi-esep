-- ──────────────────────────────────────────────────────────────
-- 7. network_kpis(p_from, p_to)
--    The top KPI row: total loss, total write-offs, fraud caught
--    (on_hold count + value), recovered value (synced to Iiko), and
--    the network unexplained gap.
-- ──────────────────────────────────────────────────────────────

create or replace function public.network_kpis(
  p_from timestamptz default now() - interval '14 days',
  p_to   timestamptz default now()
) returns jsonb
language plpgsql
stable
security definer
set search_path = public, pg_temp
as $$
declare
  result jsonb;
begin
  if public.get_my_role() not in ('reviewer','admin') then
    return jsonb_build_object(
      'total_loss', 0, 'total_writeoffs', 0, 'fraud_caught_count', 0,
      'fraud_caught_value', 0, 'recovered_value', 0, 'unexplained_gap', 0);
  end if;

  -- Write-off-based KPIs, aggregated once across the window.
  select jsonb_build_object(
    'total_loss',          coalesce(sum(w.value_cost),0),
    'total_writeoffs',     count(*),
    'fraud_caught_count',  count(*) filter (where w.status = 'on_hold'),
    'fraud_caught_value',  coalesce(sum(w.value_cost) filter (where w.status = 'on_hold'),0),
    'recovered_value',     coalesce(sum(w.value_cost) filter (where w.iiko_sync_status = 'synced'),0)
  )
  into result
  from public.writeoffs w
  where w.created_at >= p_from and w.created_at < p_to;

  -- Network unexplained gap: one row per proxied store (theoretical −
  -- documented), never multiplied by write-off count.
  select jsonb_set(
    result,
    '{unexplained_gap}',
    to_jsonb(coalesce((
      select sum(greatest(p.theoretical_usage - sdoc.doc_loss, 0))
      from public.store_usage_proxy p
      cross join lateral (
        select coalesce(sum(w.value_cost),0) as doc_loss
        from public.writeoffs w
        where w.location_id = p.store_id
          and w.created_at >= p_from and w.created_at < p_to
      ) sdoc
      where p.theoretical_usage > sdoc.doc_loss
    ), 0))
  ) into result;

  return result;
end;
$$;

revoke execute on function public.network_kpis(timestamptz, timestamptz) from anon, public;
grant  execute on function public.network_kpis(timestamptz, timestamptz) to authenticated;

-- ──────────────────────────────────────────────────────────────
-- 8. tower_analytics(p_from, p_to)
--    Single round trip: the whole Tower payload (KPIs + per-store +
--    per-format + per-city + per-cluster) as one jsonb document. The
--    page calls this once; everything else is presentation.
-- ──────────────────────────────────────────────────────────────

create or replace function public.tower_analytics(
  p_from timestamptz default now() - interval '14 days',
  p_to   timestamptz default now()
) returns jsonb
language plpgsql
stable
security definer
set search_path = public, pg_temp
as $$
begin
  if public.get_my_role() not in ('reviewer','admin') then
    return jsonb_build_object(
      'kpis', public.network_kpis(p_from, p_to),
      'perStore', '[]'::jsonb,
      'perFormat', '[]'::jsonb,
      'perCity', '[]'::jsonb,
      'perCluster', '[]'::jsonb,
      'from', p_from, 'to', p_to);
  end if;

  return jsonb_build_object(
    'kpis',       public.network_kpis(p_from, p_to),
    'perStore',   coalesce((select jsonb_agg(to_jsonb(t) order by t.unexplained_gap desc nulls last, t.total_loss desc)
                            from public.per_store_metrics(p_from, p_to) t),   '[]'::jsonb),
    'perFormat',  coalesce((select jsonb_agg(to_jsonb(t) order by
                            case t.format when 'kiosk' then 1 when 'mall' then 2 when 'magnum' then 3
                                          when 'market' then 4 when 'street' then 5 else 99 end)
                            from public.per_format_metrics(p_from, p_to) t),  '[]'::jsonb),
    'perCity',    coalesce((select jsonb_agg(to_jsonb(t) order by t.total_loss desc, t.city)
                            from public.per_city_metrics(p_from, p_to) t),    '[]'::jsonb),
    'perCluster', coalesce((select jsonb_agg(to_jsonb(t) order by t.city, t.cluster_name)
                            from public.per_cluster_metrics(p_from, p_to) t),'[]'::jsonb),
    'from', p_from, 'to', p_to
  );
end;
$$;

revoke execute on function public.tower_analytics(timestamptz, timestamptz) from anon, public;
grant  execute on function public.tower_analytics(timestamptz, timestamptz) to authenticated;

